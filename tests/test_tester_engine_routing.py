# =========================================================================== #
# C. Backward compatibility (no engine) - GUARDS, must STAY green
# =========================================================================== #
def test_llm_path_still_functional_without_engine(tmp_path, monkeypatch):
    ws = _seed(tmp_path)
    monkeypatch.setattr(
        llm, "generate_json",
        lambda prompt, system="", model=None: {
            "explanation": "e", "filename": "main.py", "new_content": "x = 1\n"})
    result = TesterBrain(_executor()).suggest_fix(ws, "main.py", ERROR, PLAN)
    assert result["new_content"] == "x = 1\n"          # legacy llm path unchanged


def test_no_breaking_constructor():
    assert TesterBrain(_executor()) is not None        # legacy 1-arg construction
    assert TesterBrain(_executor(), model="custom-model").model == "custom-model"
